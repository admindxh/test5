package com.rimi.ctibet.portal.controller.vo;

public class TravelVo {
		
	private String imgrurl;
	private String title;
	private Integer  viewcount;
	private Integer  favotecount;
	private String  visitUrl;
	public String getImgrurl() {
		return imgrurl;
	}
	public void setImgrurl(String imgrurl) {
		this.imgrurl = imgrurl;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Integer getViewcount() {
		return viewcount;
	}
	public void setViewcount(Integer viewcount) {
		this.viewcount = viewcount;
	}
	public Integer getFavotecount() {
		return favotecount;
	}
	public void setFavotecount(Integer favotecount) {
		this.favotecount = favotecount;
	}
	public String getVisitUrl() {
		return visitUrl;
	}
	public void setVisitUrl(String visitUrl) {
		this.visitUrl = visitUrl;
	}
	
	
}
