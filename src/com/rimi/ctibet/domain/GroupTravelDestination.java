package com.rimi.ctibet.domain;

public class GroupTravelDestination {
     private int id;
     private String destinationCode;
     private String groupTravelCode;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDestinationCode() {
		return destinationCode;
	}

	public void setDestinationCode(String destinationCode) {
		this.destinationCode = destinationCode;
	}

	public String getGroupTravelCode() {
		return groupTravelCode;
	}

	public void setGroupTravelCode(String groupTravelCode) {
		this.groupTravelCode = groupTravelCode;
	}
}
