package com.rimi.ctibet.domain;

public class GroupTravelView {
	
	private int id;
	private String viewCode;
	private String groupTravelCode;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getViewCode() {
		return viewCode;
	}
	public void setViewCode(String viewCode) {
		this.viewCode = viewCode;
	}
	public String getGroupTravelCode() {
		return groupTravelCode;
	}
	public void setGroupTravelCode(String groupTravelCode) {
		this.groupTravelCode = groupTravelCode;
	}
	
	


}
